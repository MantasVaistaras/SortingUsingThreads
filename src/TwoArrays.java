
public class TwoArrays {
	
	private String[] firstSortedArray;
	private String[] secondSortedArray;
	
	public TwoArrays(String[] array1, String[] array2) {
		this.firstSortedArray = array1;
		this.secondSortedArray = array2;
	}
	
	public String[] getFirsSortedArray() {
		return this.firstSortedArray;
	}
	public String[] getSecondSortedArray() {
		return this.secondSortedArray;
	}

}
